package com.t4zb.self_improvement.model;

public class Clothes {

    private String clothesId;
    private String clothesResourceUrl;
    private int clothesColor;

    public String getClothesId() {
        return clothesId;
    }

    public void setClothesId(String clothesId) {
        this.clothesId = clothesId;
    }

    public String getClothesResourceUrl() {
        return clothesResourceUrl;
    }

    public void setClothesResourceUrl(String clothesResourceUrl) {
        this.clothesResourceUrl = clothesResourceUrl;
    }

    public int getClothesColor() {
        return clothesColor;
    }

    public void setClothesColor(int clothesColor) {
        this.clothesColor = clothesColor;
    }
}
